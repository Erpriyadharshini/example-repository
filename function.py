a=int(input('Enter the value a:'))
b=int(input('Enter the value b:'))
def sum():
    return a + b
def sub():
    return a - b
def mul():
    return a * b
def div():
    return a / b
def mod():
    return a % b
print("sum:",sum())
print('sub:',sub())
print('mul:',mul())
print("div:",div())
print('mod:',mod())